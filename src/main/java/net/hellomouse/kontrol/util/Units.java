package net.hellomouse.kontrol.util;

/**
 * Electrical unit definitions
 * @author Bowserinator
 */
public class Units {
    // No unit
    public static final String UNITLESS = "";

    // Electrical units
    public static final String
            VOLT       = "V",   // Electric potential
            DELTA_VOLT = "ΔV",  // Electric potential difference
            ABS_VOLT   = "|V|", // Absolute electric potential relative to ground
            OHM        = "Ω",   // Resistance
            FARAD      = "F",   // Capacitance
            HENRY      = "H",   // Inductance
            AMP        = "A",   // Current
            WATT       = "W";   // Power

    // Misc units
    public static final String
            CELSIUS = "°C";  // Temperature
}
